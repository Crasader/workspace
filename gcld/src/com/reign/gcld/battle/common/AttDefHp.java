package com.reign.gcld.battle.common;

public class AttDefHp
{
    public int att;
    public int def;
    public int hp;
    
    public AttDefHp() {
        this.att = 0;
        this.def = 0;
        this.hp = 0;
    }
    
    public AttDefHp(final int att, final int def, final int hp) {
        this.att = 0;
        this.def = 0;
        this.hp = 0;
        this.att = att;
        this.def = def;
        this.hp = hp;
    }
}

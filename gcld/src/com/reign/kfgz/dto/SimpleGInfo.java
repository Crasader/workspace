package com.reign.kfgz.dto;

public class SimpleGInfo
{
    String pic;
    String gName;
    int gLv;
    int quality;
    
    public String getPic() {
        return this.pic;
    }
    
    public void setPic(final String pic) {
        this.pic = pic;
    }
    
    public String getgName() {
        return this.gName;
    }
    
    public void setgName(final String gName) {
        this.gName = gName;
    }
    
    public int getgLv() {
        return this.gLv;
    }
    
    public void setgLv(final int gLv) {
        this.gLv = gLv;
    }
    
    public int getQuality() {
        return this.quality;
    }
    
    public void setQuality(final int quality) {
        this.quality = quality;
    }
}

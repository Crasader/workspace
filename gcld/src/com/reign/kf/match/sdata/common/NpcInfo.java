package com.reign.kf.match.sdata.common;

public class NpcInfo
{
    public int att;
    public int def;
    public int armyHp;
    public int troopHp;
    
    public NpcInfo(final int att, final int def, final int armyHp, final int troopHp) {
        this.att = att;
        this.def = def;
        this.armyHp = armyHp;
        this.troopHp = troopHp;
    }
}
